export function getWebConfig(): WebConfig {
  const apiBaseUrl = envUrl("BERRY_WEB_API_BASE_URL");
  const demoMode = process.env.BERRY_WEB_DEMO_MODE === "false" ? false : !apiBaseUrl;
  const defaults = demoMode ? DEFAULT_CONFIG : hostedDefaults(apiBaseUrl);
  const parsed = WebConfigSchema.parse({
    ...defaults,
    apiBaseUrl,
    deploymentMode: parseDeploymentMode(process.env.DEPLOYMENT_MODE ?? process.env.BERRY_DEPLOYMENT_MODE ?? defaults.deploymentMode),
    workspaceId: process.env.BERRY_WEB_WORKSPACE_ID ?? defaults.workspaceId,
    workspacePath: process.env.BERRY_WEB_WORKSPACE_PATH ?? defaults.workspacePath,
    activeOrganizationId: process.env.BERRY_WEB_ORG_ID ?? defaults.activeOrganizationId,
    organizations: parseJsonEnv("BERRY_WEB_ORGS_JSON", defaults.organizations),
    rolePermissions: parseJsonEnv("BERRY_WEB_ROLE_PERMISSIONS_JSON", defaults.rolePermissions),
    featureFlags: parseJsonEnv("BERRY_WEB_FEATURE_FLAGS_JSON", defaults.featureFlags),
    departments: parseJsonEnv("BERRY_WEB_DEPARTMENTS_JSON", defaults.departments),
    ssoConnections: parseJsonEnv("BERRY_WEB_SSO_CONNECTIONS_JSON", defaults.ssoConnections),
    resourceAcls: parseJsonEnv("BERRY_WEB_RESOURCE_ACLS_JSON", defaults.resourceAcls),
    budgetLimits: parseJsonEnv("BERRY_WEB_BUDGET_LIMITS_JSON", defaults.budgetLimits),
    usageDashboards: parseJsonEnv("BERRY_WEB_USAGE_DASHBOARDS_JSON", defaults.usageDashboards),
    billingSummaries: parseJsonEnv("BERRY_WEB_BILLING_SUMMARIES_JSON", defaults.billingSummaries),
    modelPolicies: parseJsonEnv("BERRY_WEB_MODEL_POLICIES_JSON", defaults.modelPolicies),
    modelDefaults: parseJsonEnv("BERRY_WEB_MODEL_DEFAULTS_JSON", defaults.modelDefaults),
    policyVersions: parseJsonEnv("BERRY_WEB_POLICY_VERSIONS_JSON", defaults.policyVersions),
    auditSettings: parseJsonEnv("BERRY_WEB_AUDIT_SETTINGS_JSON", defaults.auditSettings),
    auditEvents: parseJsonEnv("BERRY_WEB_AUDIT_EVENTS_JSON", defaults.auditEvents),
    auditExports: parseJsonEnv("BERRY_WEB_AUDIT_EXPORTS_JSON", defaults.auditExports),
    platformTenants: parseJsonEnv("BERRY_WEB_PLATFORM_TENANTS_JSON", defaults.platformTenants),
    platformUsage: parseJsonEnv("BERRY_WEB_PLATFORM_USAGE_JSON", defaults.platformUsage),
    platformAuthorized: process.env.BERRY_WEB_PLATFORM_AUTHORIZED === "true" || (demoMode && defaults.platformAuthorized),
    sandboxTerminalWsUrl: envUrl("BERRY_WEB_SANDBOX_TERMINAL_WS_URL"),
    demoMode,
    providers: parseJsonEnv("BERRY_WEB_PROVIDERS_JSON", defaults.providers),
    mcpServers: parseJsonEnv("BERRY_WEB_MCP_HTTP_JSON", defaults.mcpServers),
    skills: parseJsonEnv("BERRY_WEB_SKILLS_JSON", defaults.skills),
  });
  return parsed;
}

